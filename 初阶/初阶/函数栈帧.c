// 函数栈帧（Stack Frame）是程序运行时用于管理函数调用和局部变量的内存结构。
// 它是 调用栈（Call Stack）的一部分，每当一个函数被调用时，系统会为其分配一个栈帧，用于存储函数的局部变量、参数、返回地址等信息。
// 函数执行结束后，其栈帧会被销毁。
// 
// 1. 栈帧的主要作用包括
// 存储局部变量：函数内部定义的变量
// 保存函数参数：传递给函数的参数值
// 记录返回地址：函数执行完毕后返回到调用者的位置
// 保存寄存器状态：保护调用者的寄存器值，避免被当前函数修改
// 
// 2. 栈帧的结构
// 一个典型的栈帧包含以下部分（从高地址到低地址）：
//   1. 返回地址		 函数执行完毕后返回到调用者的地址
//   2. 保存的寄存器  调用者的寄存器值（如EBP、ESI、EDI等）
//   3. 局部变量		 函数内部定义的变量
//   4. 参数			 传递给函数的参数（从右到左压栈）
//   5. 调用者栈帧	 调用者的栈帧（当前栈帧的底部）
// 
// 3. 栈帧的创建与销毁
// (1) 栈帧的创建
//   当一个函数被调用时，以下步骤会发生：
//   1. 压入返回地址：将函数执行完毕后应返回的地址压入栈中
//   2. 保存调用者的栈帧基址：将当前栈帧基址（EBP）压入栈中
//   3. 设置新的栈帧基址：将栈指针（ESP）的值赋给EBP，作为新栈帧的基址
//   4. 分配局部变量空间：调整ESP的值，为局部变量分配空间
//   5. 保存寄存器状态：将需要保护的寄存器值压入栈中
// (2) 栈帧的销毁
//   当函数执行完毕时，以下步骤会发生：
//   1. 恢复寄存器状态：从栈中弹出保存的寄存器值
//   2. 释放局部变量空间：调整ESP的值，释放局部变量占用的空间
//   3. 恢复调用者的栈帧基址：从栈中弹出调用者的EBP值
//   4. 返回到调用者：从栈中弹出返回地址，跳转到该地址继续执行
// 
// 4. 栈帧的调试
// 在调试程序时，可以使用“调用堆栈”窗口来查看栈帧
// 
// 5. 栈帧的常见问题
//   1. 栈溢出（Stack Overflow）：
//		原因：递归调用过深或局部变量占用空间过大
//		解决方法：优化递归、减少局部变量大小或使用堆内存
//   2. 栈破坏（Stack Corruption）：
//		原因：数组越界、指针错误等
//		解决方法：检查数组和指针的使用
